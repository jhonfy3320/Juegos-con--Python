'''
Enteros
Los enteros son un tipo de datos que representan números sin decimales. En Python, se definen simplemente escribiendo el número sin ningún decimal. Por ejemplo, 5 es un entero.
Aquí tienes un ejemplo de cómo se define y se utiliza un entero en Python:'''
# Definición de un entero
numero_entero = 10
print(f"El número entero es: {numero_entero}")

'''
Flotantes
Los flotantes o números de punto flotante son un tipo de datos que representan números reales, es decir, números que pueden tener una parte decimal. En Python, se definen escribiendo el número con un punto decimal. Por ejemplo, 5.0 es un flotante.
Aquí tienes un ejemplo de cómo se define y se utiliza un flotante en Python:'''
# Definición de un flotante
numero_flotante = 10.5
print(f"El número flotante es: {numero_flotante}")

'''
Booleanos
Los booleanos son un tipo de datos que representan verdadero o falso. En Python, se definen como True para verdadero y False para falso.
Aquí tienes un ejemplo de cómo se define y se utiliza un booleano en Python:'''
# Definición de un booleano
valor_booleano = True
print(f"El valor booleano es: {valor_booleano}")

